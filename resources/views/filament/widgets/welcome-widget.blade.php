            <h2>Choosing Your Magic Cards</h2>
            <p>This small system is designed for a small group of friends to review a shared set of magic cards where-ever we are in the world.</p>
            <p>It's a simple system, you can add cards to the system, and then review them with your friends.</p>

            <h3>To Use</h3>
            <p>Go to the Cards page to look at all the cards. You can filter the list in various ways. The idea is that, for each card, you say whether you want it or not with the following options:
            <ul>
                <li><b>Nope</b> - I'm not interested</li>
                <li><b>Yes</b> - I'd like this card, but don't mind if I don't get it.</li>
                <li><b>Very Yes</b> - I'd really like this card.</li>
                <li><b>OMG Yes</b> - I would be quite disappointed if I didn't get this card.</li>
            </ul>
            </p>

            <p>The Cards page has a set of tabs, which let you filter by cards you have not reviewed, or cards you have put into the above categories. You can change a card's category at any time. </p>

            <h3>1 by 1 Review</h3>
            <p>If you want to look at 1 card at a time and make some quick judgements as a first pass, you can use the "Cards 1by1 review" page. This will take you through every card you have not already reviewed, and present the 4 options to you. When you click an option, it will move to the next card. You can filter the list (e.g., if you want to go through only red creatures). </p>
            <p>You can always change your review on the main Cards page, so don't be worried that your quick clicking is final!</p>

